; ============================================================================
; tss_asm.asm - TSS 汇编支持函数
; ============================================================================

[BITS 32]

[GLOBAL tss_flush]

; void tss_flush(uint16_t tss_selector)
; 加载 TSS 到任务寄存器
tss_flush:
    mov ax, [esp + 4]    ; 获取 TSS 选择子参数
    ltr ax               ; 加载任务寄存器
    ret
